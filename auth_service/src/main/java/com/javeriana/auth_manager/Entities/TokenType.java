package com.javeriana.auth_manager.Entities;

/**
 * Enumeración que define los diferentes tipos de tokens utilizados en el sistema de autenticación.
 * El único tipo de token definido actualmente es "Bearer".
 */
public enum TokenType {
  /**
   * Tipo de token "Bearer".
   * Este tipo de token se utiliza comúnmente en esquemas de autenticación basados en tokens,
   * como el estándar de autenticación HTTP Bearer.
   */
  BEARER
}
